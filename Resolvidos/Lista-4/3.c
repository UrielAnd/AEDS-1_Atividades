/*~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

#Pergunta:
    Faça um algoritmo para imprimir os números de 1 a 10 utilizando
uma estrutura PARA e um contado.

~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~*/
#include <stdio.h>

int main()
{
//PROCESSAMENTO_______________________________________________________________________________    
    for(int repet=1;repet<=10;repet++)                     //Estrutura PARA./ Variável contadora repet./incremento do repet.//
    {
    //OUTPUT__________________________________________________________________________________    
        printf("%d\n", repet);                           //Impressão do repet.//
    }

    return 0;
}


