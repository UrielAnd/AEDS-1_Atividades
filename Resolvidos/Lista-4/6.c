/*~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

#Pergunta:
Faça um algoritmo que imprima na tela os 10 primeiros números inteiros ~
maiores que 100 utilizando um a estrutura de repetição.

~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~*/
#include <stdio.h>

int main()
{
//PROCESSAMENTO_____________________________________________________________________________________    
    for(int repet=101;repet<=110;repet++)         //Ciclo./Variável de repetição./Incremento.//
    {
    //OUTPUT_____________________________________________________________________________________ 
        printf("%d\n", repet);                          //Impressão dos n°.//
    }
    return 0;
}


